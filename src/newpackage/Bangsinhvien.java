    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        StringBuilder sb = new StringBuilder();
        DataVali.validateEmpty(txtMa, sb, "mã sv ko đc để trống");
        DataVali.validateEmpty(txtEmail, sb, "Emai sv ko đc để trống");
        DataVali.validateEmpty(txtHoTen, sb, "Emai sv ko đc để trống");
        DataVali.validateEmpty(txtSDT, sb, "Emai sv ko đc để trống");
        if (sb.length()>0) {
            MessageDialong.showErrorDialog(parentForm, sb.toString(), "Lỗi");
            return;
        }
        try {
            SinhVien sv = new SinhVien();
            sv.setMaSinhVien(txtMa.getText());
            sv.setHoTen(txtHoTen.getText());
            sv.setEmail(txtEmail.getText());
            sv.setSoDT(txtSDT.getText());
            sv.setDiaChi(txtDiaChi.getText());
            sv.setGioiTinh(rdoNam.isSelected() ?1:0);
            
            SinhVienDao dao = new SinhVienDao();
            if (dao.insert(sv)) {
                MessageDialong.showMessageDialog(parentForm, "SV đã dc lưu", "Thông báo");
            }else{
                MessageDialong.showMessageDialog(parentForm, "SV Ko dc lưu", "Cảnh báo");
            }
            loaData();
        } catch (Exception e) {
            e.printStackTrace();
            MessageDialong.showErrorDialog(parentForm
                    ,e.getMessage(), "Lỗi");
        }
    }//GEN-LAST:event_jButton3ActionPerformed

