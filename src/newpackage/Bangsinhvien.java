    private void tblSinhvienMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tblSinhvienMouseClicked
        try {
            int row = tblSinhvien.getSelectedRow();
            
            if (row >= 0) {
                String id = (String) tblSinhvien.getValueAt(row, 0);
                SinhVienDao dao = new SinhVienDao();
                SinhVien sv = dao.findByid(id);
                if (sv != null) {
                    txtMa.setText(sv.getMaSinhVien());
                    txtHoTen.setText(sv.getHoTen());
                    txtEmail.setText(sv.getEmail());
                    txtSDT.setText(sv.getSoDT());
                    txtDiaChi.setText(sv.getDiaChi());
                    rdoNam.setSelected(sv.getGioiTinh()==1?true:false); 
                }
            }
            
        } catch (Exception e) {
              e.printStackTrace();
            MessageDialong.showErrorDialog(parentForm
                    ,e.getMessage(), "Lỗi");
        }
    }//GEN-LAST:event_tblSinhvienMouseClicked


