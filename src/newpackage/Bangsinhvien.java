    private void loaData(){
        try {
           SinhVienDao dao = new SinhVienDao();
           List<SinhVien> list = dao.findAll();
           tblModel.setRowCount(0);
            for (SinhVien x : list) {
                tblModel.addRow(new Object[]{
                    x.getMaSinhVien(),x.getHoTen(),x.getEmail(),x.getSoDT(),x.getGioiTinh()==1? "Nam":"Nữ",x.getDiaChi()
                });
            }
            tblModel.fireTableDataChanged();
        } catch (Exception e) {
            JOptionPane.showMessageDialog(parentForm, e);
        }
    }

