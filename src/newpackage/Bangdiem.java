    private void btnLuuActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnLuuActionPerformed
        StringBuilder sb = new StringBuilder();
        DataVali.validateEmpty(txtMasinhvien, sb, "Chưa nhập mã Sv");
        DataVali.validateEmpty(txtTienganh, sb, "Chưa nhập Tiếng anh");
        DataVali.validateEmpty(txtTinhọc, sb, "Chưa nhập Tin Học");
        DataVali.validateEmpty(txtGDTC, sb, "Chưa nhập mã GDTC");
        if (sb.length() > 0) {
            MessageDialong.showErrorDialog(parenForm, sb.toString(), "lỗi");
            return;
        }
        try {
            BangDiem bd = new BangDiem();
            bd.setMaSinhVien(txtMasinhvien.getText());
            bd.setTiengAnh(Float.parseFloat(txtTienganh.getText()));
            bd.setTinHoc(Float.parseFloat(txtTinhọc.getText()));
            bd.setGDTC(Float.parseFloat(txtGDTC.getText()));

            BangDiemDao dao = new BangDiemDao();
            if (dao.findByMaSinhVien(txtMasinhvien.getText()) != null) {
                if (MessageDialong.showConfirmDialog(parenForm, "bạn có muốn cập nhật điểm không", "hỏi")==JOptionPane.NO_OPTION) {
                    return;
                }
                if (dao.insert(bd)) {
                    MessageDialong.showMessageDialog(parenForm, "Bảng điểm đã dc cập nhật", "Thông Báo ");
                } else {
                    MessageDialong.showMessageDialog(parenForm, "Bảng điểm không dc cập nhật", "Thông Báo ");
                }
            } else
            { 
                if (dao.insert(bd)) {
                MessageDialong.showMessageDialog(parenForm, "Bảng điểm đã dc lưu", "Thông Báo ");
            } else {
                MessageDialong.showMessageDialog(parenForm, "Bảng điểm không dc lưu", "Thông Báo ");
            }}
loadBangDiem();
initTable();
        } catch (Exception e) {
            e.printStackTrace();
            MessageDialong.showErrorDialog(parenForm, e.getMessage(), "Lỗi");
        }

    }//GEN-LAST:event_btnLuuActionPerformed

