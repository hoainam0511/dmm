    private void loadBangDiem(){
        try {
            BangDiemDao dao = new BangDiemDao();
            List<BangDiem> list = dao.findTop(3);
            tableModel.setRowCount(0);
            for (BangDiem x : list) {
               tableModel.addRow(new Object[]{
                   x.getMaSinhVien(),x.getTiengAnh(),x.getTinHoc(),x.getGDTC(),
                   (x.getTiengAnh()+x.getTinHoc()+x.getGDTC())/3
               });
            }
            tableModel.fireTableDataChanged();
        } catch (Exception e) {
            e.printStackTrace();
            MessageDialong.showErrorDialog(parenForm, e.getMessage(), "Lỗi");
        }
    }
    
